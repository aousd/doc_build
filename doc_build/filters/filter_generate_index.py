def normalize_word(word: str) -> str:
    normalized = word.lower()
    normalized = normalized.strip()
    normalized = normalized.strip(string.punctuation + string.digits)
    normalized = normalized if normalized not in stop_words else ""
    normalized = normalized if not normalized.startswith("https:") else ""
    # TODO lemmatization from NLTK to replace "instances" with "instance"?
    return normalized


words = defaultdict(int)


def normalized_words(key, value, format, metadata):
    if key == "Str":
        words[normalize_word(value)] += 1

    OUTPUT = metadata["OUTPUT"]["c"][0]["c"]

    sorted_dict = dict(sorted(words.items(), key=lambda item: item[1]))
    with open(OUTPUT, "w") as f:
        for k, v in sorted_dict.items():
            f.write(f"{k}\t{v}\n")


